package sampleProj;

public class Circle {
	private int x;
	private int y;
	public Circle() {
		
	}
public Circle(int x , int y) {
		x=x;
		y=y;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}

}
